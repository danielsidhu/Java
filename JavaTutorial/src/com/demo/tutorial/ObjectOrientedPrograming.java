package com.demo.tutorial;

//There are 4 Pillars to OOP
public class ObjectOrientedPrograming {

	/**
	 * Encapsulation is the principle when you bundle data aka fields and the
	 * manipulation of the data aka methods into one unit
	 */

	/**
	 * Abstraction is the principle when you reduce the complexity by hiding
	 * unnecessary details of certain implementations.
	 */

	/**
	 * Inheritance is a principle of when a child object inherits or re-uses the
	 * methods of its parent class
	 */

	/**
	 * Polymorphism is a principle of having many forms and examples are method
	 * overloading and method overriding
	 */
}
